#!/usr/bin/env node
/*
 * Generate docs/SKILLS.md — a browsable catalog of every skill.
 *
 * Reads the frontmatter (name + description) of every
 * problem-consciousness/skills/<name>/SKILL.md, groups skills by an ordered,
 * first-match rule set, and writes a deterministic Markdown catalog (stable
 * group order; skills sorted alphabetically within a group).
 *
 *   node scripts/gen-skill-catalog.js           # write docs/SKILLS.md
 *   node scripts/gen-skill-catalog.js --check    # CI: fail if the file is stale
 *
 * The catalog is a generated artifact — like check-version.sh, it must never
 * drift from source. CI runs --check so adding/renaming a skill without
 * regenerating fails the build.
 */
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SKILLS_DIR = path.join(ROOT, 'problem-consciousness', 'skills');
const OUT = path.join(ROOT, 'docs', 'SKILLS.md');

// Ordered categories. First matching rule wins, so more specific prefixes
// (framework-, geometry-, …) are placed before the generic critique bucket.
const CATEGORIES = [
  ['Governance', 'The constitution: runs above every method and keeps verdicts honest.',
    n => ['meta-problem-layer', 'problem-state-machine', 'peer-cross-check'].includes(n)],
  ['Framework schema (問題框架)', 'Build and stress-test the problem-framing schema itself.',
    n => /^framework-/.test(n)],
  ['Extraction (抽取)', 'Pull structured evidence from full text without losing context.',
    n => /^extraction-/.test(n)],
  ['Validation (驗證)', 'Gate claims against power, symmetry, tautology and false-kill.',
    n => /^validation-/.test(n)],
  ['Downstream method (幾何 / 流形 / 生命週期)', 'Geometry, manifold and lifecycle analyses — used only under their validity gates.',
    n => /^(geometry|manifold|hmm)-/.test(n) || n === 'topology-language'],
  ['Harvest / keyword / anchor (採集·錨定)', 'Turn a framed problem into corpora: harvest, keyword design, anchoring.',
    n => /^(harvest|keyword)-/.test(n) ||
      ['literature-harvest', 'problem-anchored-harvest', 'narrow-keyword-backfill',
       'systematic-anchoring-discipline', 'mechanism-bridge'].includes(n)],
  ['Loops & orchestration (環路)', 'Multi-step loops that drive a problem through the pipeline.',
    n => /^pc-.*-loop$/.test(n) ||
      ['conjecture-refutation-loop', 'pipeline-rectify', 'rectify-reverify'].includes(n)],
  ['Critique & verify (批判·查證)', 'Adversarial lenses, conclusion supervision, and the citation gate.',
    n => /^critic-/.test(n) || /^expert-critique/.test(n) || /^conclusion-/.test(n) ||
      ['citation-gate', 'critique-harvest', 'register-distortion'].includes(n)],
  ['Framing & problem model (問題框定)', 'Frame, identify, decompose and qualify the problem.',
    n => /^problem-/.test(n) || ['wellposedness-gate', 'ssif-reasoning-os'].includes(n)],
  ['Domain & analysis (領域方法)', 'Cross-cutting analysis methods and consistency checks.',
    n => ['domain-analysis-methodology', 'five-axis-consistency', 'three-layer-relations',
          'patent-frontier-pc', 'frontier-live-scan', 'whitespace-definition'].includes(n)],
  ['Other', 'Uncategorized — if a skill lands here, extend the rules in this script.',
    () => true],
];

function firstSentence(desc) {
  // Descriptions are single-line YAML. Take up to the first Chinese/ASCII
  // full stop; cap length so the catalog stays scannable.
  const s = String(desc).trim();
  const m = s.match(/^[^。.]*[。.]/);
  let out = m ? m[0] : s;
  if (out.length > 140) out = out.slice(0, 139).trimEnd() + '…';
  return out.trim();
}

function readSkills() {
  const dirs = fs.readdirSync(SKILLS_DIR, { withFileTypes: true })
    .filter(d => d.isDirectory()).map(d => d.name);
  const skills = [];
  for (const dir of dirs) {
    const p = path.join(SKILLS_DIR, dir, 'SKILL.md');
    let txt;
    try { txt = fs.readFileSync(p, 'utf8'); } catch (e) { continue; }
    const fm = txt.match(/^---\s*\n([\s\S]*?)\n---/);
    const block = fm ? fm[1] : txt;
    const nameM = block.match(/^name:\s*(.+)$/m);
    const descM = block.match(/^description:\s*(.+)$/m);
    if (!nameM) continue;
    skills.push({
      name: nameM[1].trim(),
      desc: descM ? firstSentence(descM[1]) : '',
    });
  }
  return skills;
}

function categorize(skills) {
  const groups = CATEGORIES.map(([title, blurb]) => ({ title, blurb, items: [] }));
  for (const s of skills) {
    for (let i = 0; i < CATEGORIES.length; i++) {
      if (CATEGORIES[i][2](s.name)) { groups[i].items.push(s); break; }
    }
  }
  for (const g of groups) g.items.sort((a, b) => a.name.localeCompare(b.name));
  return groups.filter(g => g.items.length > 0);
}

function render(skills, groups) {
  const lines = [];
  lines.push('# Skill Catalog');
  lines.push('');
  lines.push('<!-- GENERATED by scripts/gen-skill-catalog.js — do not edit by hand.');
  lines.push('     Regenerate: node scripts/gen-skill-catalog.js -->');
  lines.push('');
  lines.push('**' + skills.length + ' skills** across ' + groups.length +
    ' groups. Each entry links its skill name to a one-line summary; open the skill' +
    " for the full method. See [ARCHITECTURE](../problem-consciousness/ARCHITECTURE.md)" +
    ' for the three-layer model.');
  lines.push('');
  lines.push('| Group | Count |');
  lines.push('|---|---|');
  for (const g of groups) lines.push('| [' + g.title + '](#' + anchor(g.title) + ') | ' + g.items.length + ' |');
  lines.push('');
  for (const g of groups) {
    lines.push('## ' + g.title + ' (' + g.items.length + ')');
    lines.push('');
    lines.push('_' + g.blurb + '_');
    lines.push('');
    for (const s of g.items) {
      lines.push('- **`' + s.name + '`** — ' + (s.desc || '_(no description)_'));
    }
    lines.push('');
  }
  return lines.join('\n').replace(/\n+$/, '\n');
}

function anchor(title) {
  // GitHub-style heading anchor: lowercase, drop non-word (keep CJK), spaces->-
  return title.toLowerCase()
    .replace(/[^\w一-鿿\s-]/g, '')
    .trim().replace(/\s+/g, '-');
}

function main() {
  const check = process.argv.includes('--check');
  const skills = readSkills();
  const groups = categorize(skills);
  const content = render(skills, groups);

  if (check) {
    let existing = '';
    try { existing = fs.readFileSync(OUT, 'utf8'); } catch (e) {}
    if (existing !== content) {
      console.error('FAIL: docs/SKILLS.md is stale. Run: node scripts/gen-skill-catalog.js');
      process.exit(1);
    }
    console.log('docs/SKILLS.md is up to date (' + skills.length + ' skills).');
    return;
  }

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, content);
  console.log('Wrote docs/SKILLS.md — ' + skills.length + ' skills, ' + groups.length + ' groups.');
}

main();
